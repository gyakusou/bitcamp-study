package com.ohoracs.basic.ex03;

public class Exam1 {

  public static void main(String[] args) {
    //정수 리터럴
    System.out.println(78); //10진수 표기법
    //78이 정수 리터럴이다.

    System.out.println(+78); //10진수 표기법
    System.out.println(-78); //10진수 표기법

    System.out.println(0116); //8진수 표기법
    ////0116 
    //64 8 6
    System.out.println(-0116); //8진수 표기법

    System.out.println(0x4e); //16진수 표기법
    System.out.println(-0x4e); //16진수 표기법

    System.out.println(0b01001110); //8진수 표기법

    //부동소수점 리터럴

    //논리 리터럴

    //문자 리터럴
  }
}

